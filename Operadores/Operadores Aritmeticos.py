# Operadores aritmeticos

x = 5
y = 10

#suma +
suma = x + y
print("La suma es:", suma)

#Resta -
resta = x - y
print("La resta es:", resta)

#Multiplicación *
multiplicacion = x * y 
print("La multiplcación es:", multiplicacion)

#Division / (Retorna un Float)
division = y / x
print("La division es:", division)

#Modulo % (Retorna el residuo de la división)
modulo = y % x
print("El modulo es:", modulo)

#Potencia **
potencia = y ** x
print("La potencia es:", potencia)

#Division entera // (Retorna un entero redondeado hacia abajo)
division_entera = y // x
print("La division entera es:", division_entera)

#Ejemplo de uso del operador modulo para determinar si un numero es par o impar
par = 7
print("El numero es par:", par % 2 == 0)

