#Operadores Asignacion

x = 5

# x = x + 3 (opcion tradicional)
x += 3  #(Opcion con operador de asignacion) Suma
print("El valor de x es:", x)

x -= 3 #Resta
print("El valor de x es:", x)

x *= 3 #Multiplicacion
print("El valor de x es:", x)

x /= 3 #Division
print("El valor de x es:", x)

x %= 2 #Modulo (Resto)
print("El valor de x es:", x)

y = 20

y //= 2 #Division entera
print("El valor de y es:", y)

y **= 3 #Exponente 10 * 10 * 10
print("El valo de y es;", y)


#WALRUS (Asignacion con operador logico "Morsa")
print(z := 3) #Nos va a dejar asignar el valor dentro de un metodo una variable
print(z)


